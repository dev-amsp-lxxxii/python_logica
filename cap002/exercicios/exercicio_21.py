"""
Converta as sequintes expressões matemáticas para que possam ser calculadas usando o interpretador Python.
10 + 20 x 30
4^2 / 30
(9^4 + 2) x 6 - 1
"""

print(f"10 + 20 x 30 -> {10 + 20 * 30}")
print(f"4^2 / 30 -> {4 ** 2 / 30}")
print(f"(9^4 + 2) x 6 - 1 -> {(9 ** 4 + 2) * 6 - 1}")
